# desenvolvendo e aplicando operadores de comparação

#       ==  Equal
#       !=  Not Equal
#       >   Greater than
#       <   Less than
#       >=  Greater than or equal to
#       <=  Less than or equal to

# vamos criar uma primeira operação para aplicar Equal [ == ]
operacao = 10 == 10
print(operacao)

# testando com strings/palavras
operacao = 'banana' == 'Banana'
print(operacao)


# aplicar Not Equal [ != ]
operacao = 35 != 22
print(operacao)

operacao = 35 != 35
print(operacao)

operacao = 'banana' != 'Banana'
print(operacao)


# aplicar > e <
operacao = 35 > 22
print(operacao)

operacao = 35 < 22
print(operacao)

operacao = 'banana' > 'Banana'
print(operacao)


# aplicar Greater/Less than or equal to
operacao = 35 >= 22
print(operacao)

operacao = 35 <= 22
print(operacao)

operacao = 'banana' >= 'Banana'
print(operacao)

operacao = 35 >= 35
print(operacao)


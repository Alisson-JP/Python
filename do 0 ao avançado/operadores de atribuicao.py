# aplicar operadores de atribuição

# adicionando valores a uma variável
# modo padrão

from re import X


x = 10
x = x + 5
print(x)


# modo reduzido com Soma
x += 5
print(x)


# modo reduzido com Subtração
x -= 5
print(x)


# modo reduzido com Multiplicação
x *= 2
print(x)


# modo reduzido com Divisão
x /= 3
print(x)


# modo reduzido com Restante/sobra [ % ]
# no exemplo abaixo temos que, 10 dividido por 3, sobra 1
x %= 3
print(x)

# no exemplo abaixo, onde 10 dividos por 5 dá um número inteiro, o resto é 0 (zero)
y = 10
y %= 5
